package com.pyxelze.roxify.core

import org.junit.Assert.assertArrayEquals
import org.junit.Test
import java.io.ByteArrayInputStream

class StegoPngHelperTest {

    private val MINIMAL_PNG: ByteArray = byteArrayOf(
        0x89.toByte(), 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
        0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52,
        0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x02, 0x00, 0x00, 0x00,
        0x90.toByte(), 0x77, 0x53, 0xDE.toByte(),
        0x00, 0x00, 0x00, 0x0C,
        0x49, 0x44, 0x41, 0x54,
        0x08, 0xD7.toByte(), 0x63, 0xF8.toByte(), 0xCF.toByte(), 0xC0.toByte(), 0x00, 0x00, 0x03, 0x01, 0x01, 0x00,
        0x18, 0xDD.toByte(), 0x8D.toByte(), 0xB0.toByte(),
        0x00, 0x00, 0x00, 0x00,
        0x49, 0x45, 0x4E, 0x44,
        0xAE.toByte(), 0x42, 0x60, 0x82.toByte()
    )

    @Test
    fun testEmbedAndExtract() {
        val payload = "Test Payload!".toByteArray(Charsets.UTF_8)

        val newPng = StegoPngHelper.embedChunk(ByteArrayInputStream(MINIMAL_PNG), payload)
        val extracted = StegoPngHelper.extractChunk(ByteArrayInputStream(newPng))

        assertArrayEquals(payload, extracted)
    }
}
